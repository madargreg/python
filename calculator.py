num1 =float(input("write your first num:"))
num3 =input("write an operator: ")
num2 =float(input("write your second num:"))

if num3 == "*":
        print(num1 * num2)
elif num3 == "/":
        print(num1 / num2)
elif num3 == "+":
        print(num1 + num2)
else:
        print(num1 - num2)

